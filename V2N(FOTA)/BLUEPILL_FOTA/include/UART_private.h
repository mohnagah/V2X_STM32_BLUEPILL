#define UART3_STOP_BITS_MODE_1   0
#define UART3_STOP_BITS_MODE_0_5 1
#define UART3_STOP_BITS_MODE_2   2
#define UART3_STOP_BITS_MODE_1_5 3

#define UART3_TRANSMITTER_STATE_DISABLE 0
#define UART3_TRANSMITTER_STATE_ENABLE  1

#define UART3_BAUDRATE_2400   2400
#define UART3_BAUDRATE_9600   9600
#define UART3_BAUDRATE_19200  19200
#define UART3_BAUDRATE_57600  57600
